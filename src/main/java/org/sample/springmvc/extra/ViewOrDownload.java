public class ViewOrDownload {
    private String view;
    private String download;
    public String getView() {
        return view;
    }
    public void setView(String view) {
        this.view = view;
    }
    public String getDownload() {
        return download;
    }
    public void setDownload(String download) {
        this.download = download;
    }

}
